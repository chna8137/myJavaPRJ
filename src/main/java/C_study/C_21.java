package C_study;

public class C_21 {
    public static void main(String[] args) {

        int a = 100;
        int b = 50;
        int result;

        result = a+b;
        System.out.printf("%d + %d = %d \n",a,b,result);

        result = a-b;
        System.out.printf("%d - %d = %d \n",a,b,result);

        result = a*b;
        System.out.printf("%d * %d = %d \n",a,b,result);

        result = a/b;
        System.out.printf("%d / %d = %d \n",a,b,result);
    }
}
